# ขอให้ผู้ใช้ป้อนตัวเลข
try:
    number = float(input("กรุณาป้อนตัวเลข: "))

    # ตรวจสอบว่าตัวเลขเท่ากับศูนย์หรือไม่
    if number == 0:
        print("ตัวเลขนี้เท่ากับศูนย์.")
    else:
        print("ตัวเลขนี้ไม่เท่ากับศูนย์.")
except ValueError:
    print("ข้อมูลที่ป้อนไม่ถูกต้อง กรุณาป้อนตัวเลขที่ถูกต้อง.")

