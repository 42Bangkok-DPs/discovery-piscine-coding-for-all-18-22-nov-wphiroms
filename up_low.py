# สคริปต์นี้จะแปลงตัวพิมพ์ใหญ่เป็นพิมพ์เล็ก และตัวพิมพ์เล็กเป็นพิมพ์ใหญ่

def main():
    # ขอให้ผู้ใช้ป้อนข้อความ
    user_input = input("กรุณาป้อนข้อความ: ")
    
    # แปลงข้อความโดยเปลี่ยนตัวพิมพ์ใหญ่เป็นพิมพ์เล็ก และตัวพิมพ์เล็กเป็นพิมพ์ใหญ่
    swapped_case = user_input.swapcase()
    
    # แสดงข้อความที่ถูกแปลงแล้ว
    print("ข้อความที่แปลงแล้ว:", swapped_case)

# ทำให้โปรแกรมสามารถรันได้โดยตรง
if __name__ == "__main__":
    main()
