#!/usr/bin/env python3

# รับข้อมูลจากผู้ใช้
try:
    user_input = int(input("กรุณาป้อนตัวเลข: "))  # ขอให้ผู้ใช้ป้อนตัวเลข
    
    if user_input > 25:
        print("ข้อผิดพลาด")  # แสดงข้อความ "ข้อผิดพลาด" ถ้าตัวเลขมากกว่า 25
    else:
        for num in range(user_input, 26):  # วนลูปจากตัวเลขที่ป้อนจนถึง 25
            print(num)  # แสดงตัวเลขทีละบรรทัด
except ValueError:
    print("ข้อผิดพลาด: กรุณาป้อนตัวเลขที่ถูกต้อง")  # แสดงข้อผิดพลาดถ้าป้อนข้อมูลไม่ใช่ตัวเลข
